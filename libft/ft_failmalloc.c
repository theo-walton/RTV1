#include <stdlib.h>

void	ft_failmalloc(void)
{
	int				*i;
	long long int	n;

	n = 1;
	i = (int*)&n;
	while (n)
	{
		while (i)
		{
			i = malloc(n);
			if (i)
				n = n + n;
		}
		n = n / 2;
		i = (int*)&n;
	}
}
