#include "libft.h"

void	ft_strrev(char *str)
{
	int i;
	int a;

	i = ft_strlen(str);
	a = 0;
	while (2 * a < i)
	{
		ft_swap(&str[a], &str[i - a - 1]);
		a++;
	}
}
