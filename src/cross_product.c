#include "rtv1.h"

void	cross_product(t_coor *result, t_coor c1, t_coor c2)
{
	result->x = c1.y * c2.z - c2.y * c1.z;
	result->y = c1.z * c2.x - c1.x * c2.z;
	result->z = c1.x * c2.y - c1.y * c2.x;
}
